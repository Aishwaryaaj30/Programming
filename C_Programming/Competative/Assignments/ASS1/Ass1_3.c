///////////////////////////////////////////////////////////
//
//  Include required header files
//
///////////////////////////////////////////////////////////

#include<stdio.h>

///////////////////////////////////////////////////////////
//
//  Function name : Display
//  Input :         -
//  Output :        -
//  Description :   Performs iteration using whil2 loop
//  Date :          08/05/2026
//  Author :        Aishwarya Mohan Jadhav
//
///////////////////////////////////////////////////////////

void Display()
{
    int i = 0;
    i = 5;

    while(i >= 1)
    {
        printf("%d\t", i);
        i--;
    }
}

///////////////////////////////////////////////////////////
//
//  Application to perform iteration
//
///////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0;
}

